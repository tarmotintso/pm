<div id="contentArea"><h1 class="first">Ostujuhis</h1>

<div id="content_document_description"><p>Netipoe kasutamise juhend:</p>

<p>1. Leia otsingu või kataloogi kaudu endale meelepärane üritus.</p>

            <p>2. Vajuta nuppu "Osta".</p>

            <p>3. Vali piirkond (saal või rõdu), kus sooviksid üritusel istuda.</p>

            <p>4. &nbsp;Vali avanenud saaliplaanilt meelepärased kohad. Kui üritusel pole saaliplaani, saab
                valikumenüüst valida ostetavate kohtade arvu. Kinnita oma valik.</p>

            <p>5. Vali piletitele sobivad soodustused või nende puudumisel veendu ostu õigsuses. Kinnita tehing.</p>

            <p>6. &nbsp;Vali endale sobiv piletite eest maksmiseks kasutatav netipank.</p>

            <p>7. Sisene oma netipanka ja soorita seal maksetehing. Kui tehing pangas tehtud, kliki netipangas nuppu
                "Tagasi kaupmehe juurde". Juhul kui ID kaardiga panka sisenemine ei õnnestu, mine
                https://www.sk.ee/tervitus ja kontrolli, et näed tervitust. Kui ei näe, siis saada e-post aadressile
                piletimaailm@piletimaailm.com ja saadame Sulle juhendi. </p>

            <p>8. Trüki välja netipiletid.</p>

            <p>9. Piletimaailma netipileti kasutamiseks on kaks võimalust:<br>Väljatrükitud netipiletit võib kasutada
                ürituse sissepääsus.<br>Netipileti võib vahetada turvamärgistusega pileti vastu Piletimaailma
                müügipunktides.</p>

<p><strong>NB!</strong> Kui Teil ei ole võimalust printida, siis jätke meelde valitud ürituse andmed.
Piletisaate müügipunktist kätte, kui esitate ürituse andmed ja isikut tõendava dokumendi. <br>

<p class="MsoPlainText">Piletimaailm ostetud pileteid tagasi ei osta ja ümber ei vaheta, juhul, kui ei ole
                teatatud teisiti.</p>

            <p>Lukustatud, kuid ostmata kohad vabanevad süsteemis umbes 50 minuti jooksul.</p>

            <p>Piletimaailm asub SA Eesti Draamateatri haldusalas ning seetõttu laekuvad kõik ülekanded SA Eesti
                Draamateatri arvelduskontole.&nbsp;</p>

            <p>Piletimaailma kinkekaardid on müügil kõigis Piletimaailma müügipunktides (v.a netipood). <br>Piletimaailma
                kinkekaarti saab vahetada piletite ostmise käigus netipoes või järgmistes teatrikassades: Eesti
                Draamateater, Pärnu Teater Endla, Ugala Teater, Kuressaare Linnateater, Teater Vanemuine, Rakvere
                Teater.</p>

            <p>Piletimaailma kinkekaardi kasutamisel netipoes tuleb vastavasse lahtrisse sisestada triipkoodi all olev
                must number (näide PM1234566). </p>

